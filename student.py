class Student:
    # The Student Class

    def __init__(self, username, password, firstname, lastname, lang, email, sms, ads, friends, pending, pendingMsgs, membership):
        self.username = username
        self.password = password
        self.firstname = firstname
        self.lastname = lastname
        self.lang = lang
        self.email = email
        self.sms = sms
        self.ads = ads
        self.friends = friends
        self.pending = pending
        self.pendingMsgs = pendingMsgs
        self.membership = membership
